    public void actualizarListaFormas(){
        if(this.getLienzo2D().getvShape() != null){
            this.listaShapes = this.getLienzo2D().getvShape();
            System.out.println(this.listaShapes.size());
            if(vPrincipal != null){
                vPrincipal.listaFiguras.removeAllItems();
                if(this.listaShapes != null){
                    for(MyShape s:listaShapes){
                        vPrincipal.listaFiguras.addItem(s);
                    }
                }
            }
        }
    }


