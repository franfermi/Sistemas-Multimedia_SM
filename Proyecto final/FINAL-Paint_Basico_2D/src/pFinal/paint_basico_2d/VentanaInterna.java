    private void formInternalFrameActivated(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameActivated
        if(this.vPrincipal != null){
            this.getLienzo2D().addLienzoListener(vPrincipal.manejador);
            if(this.getLienzo2D().getHerrSelec() != null){
                switch (this.getLienzo2D().getHerrSelec()){
                    case LAPIZ:
                        this.vPrincipal.btnLapiz.setSelected(true); 
                        this.vPrincipal.txtBarraEstado.setText("Lápiz");
                        break;
                    case LINEA:
                        this.vPrincipal.btnLinea.setSelected(true); 
                        this.vPrincipal.txtBarraEstado.setText("Línea");
                        break;
                    case RECTANGULO:
                        this.vPrincipal.btnRectangulo.setSelected(true);
                        this.vPrincipal.txtBarraEstado.setText("Rectángulo");
                        break;
                    case ELIPSE:
                        this.vPrincipal.btnElipse.setSelected(true); 
                        this.vPrincipal.txtBarraEstado.setText("Elipse");
                        break;
                }
            }
            
            this.vPrincipal.listaColores.setSelectedItem(this.getLienzo2D().getColorTrazo());
            this.actualizarListaFormas();
            
            this.strokeVPrincipal = (BasicStroke)this.getLienzo2D().getGrosor();
            this.vPrincipal.spinnerGrosor.setValue((int) strokeVPrincipal.getLineWidth());
            
            this.vPrincipal.btnEditar.setSelected(this.getLienzo2D().getEditar());
            this.vPrincipal.btnTransparencia.setSelected(this.getLienzo2D().getTransparencia());
            this.vPrincipal.btnAlisado.setSelected(this.getLienzo2D().getAlisar());
        }
    }//GEN-LAST:event_formInternalFrameActivated

    private void lienzo2DMouseMoved(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lienzo2DMouseMoved
        try {
            Color c;
            Robot r = new Robot();
            c = r.getPixelColor(evt.getXOnScreen(), evt.getYOnScreen());
            
            this.vPrincipal.txtValorRGB.setText("R:" + c.getRed() + " G:" + c.getGreen() + " B:" + c.getBlue());
        } catch (AWTException ex) {
            Logger.getLogger(VentanaInterna.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_lienzo2DMouseMoved

